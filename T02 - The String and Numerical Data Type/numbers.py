# numbers.py

num1 = int(input("Enter the first integer: "))
num2 = int(input("Enter the second integer: "))
num3 = int(input("Enter the third integer: "))

# math operations with inputs
sum_of_numbers = num1 + num2 + num3
print("The sum of all the numbers:", sum_of_numbers)

first_minus_second = num1 - num2
print("The first number minus the second number:", first_minus_second)

third_multiplied_first = num3 * num1
print("The third number multiplied by the first number:", third_multiplied_first)

sum_divided_third = sum_of_numbers / num3
print("The sum of three numbers divided by the third number:", sum_divided_third)
